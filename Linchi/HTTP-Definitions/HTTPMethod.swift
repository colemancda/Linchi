/// Enum describing an HTTPMethod
public enum HTTPMethod: String {    
    case GET = "GET"
    case POST = "POST"
    case HEAD = "HEAD"
    case DELETE = "DELETE"
    case OPTIONS = "OPTIONS"
    case PUT = "PUT"
    case CONNECT = "CONNECT"
    case PATCH = "PATCH"
    case TRACE = "TRACE"
}